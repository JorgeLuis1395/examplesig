    public String buscar() {

        if (empleadoBean.getEmpleadoAdicional() == null) {
            MensajesErrores.advertencia("Seleccione un empleado primero");
            return null;
        }

        Map parametros = new HashMap();
        String where = "o.entidad=:entidad";
        parametros.put("entidad", empleadoBean.getEmpleadoAdicional().getEntidad());
        parametros.put(";where", where);
        try {
            listaEmpleado = ejbEmpleados.encontarParametros(parametros);
        } catch (ConsultarException ex) {
            Logger.getLogger(BancoBean.class.getName()).log(Level.SEVERE, null, ex);
        }

        return null;

    }

